def main():
    db = SessionLocal()
    created = 0
    skipped = 0
    try:
        for data in DOCTORS:
            existing = db.query(Doctor).filter(Doctor.slug == data["slug"]).first()
            if existing:
                skipped += 1
                continue
            email_exists = db.query(Doctor).filter(Doctor.email == data["email"]).first()
            if email_exists:
                # Update with new fields
                for k, v in data.items():
                    if k != "email":
                        setattr(email_exists, k, v)
                db.commit()
                skipped += 1
                continue
            doc = Doctor(**data)
            db.add(doc)
            created += 1
        db.commit()
        print(f"Seeded {created} new doctors, {skipped} already existed or updated.")
    except Exception as e:
        db.rollback()
        print(f"Error: {e}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    main()
